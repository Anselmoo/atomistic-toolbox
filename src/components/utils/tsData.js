function parseLabel(label) {
  const parts = label.split('_')
  const superscriptDigits = ['', '', '', '', '', '', '', '', '', '']
  const superscript = parts[0]
    .split('')
    .map((digit) => superscriptDigits[digit])
    .join('')
  const subscriptDigits = ['', '', '', '', '', '', '', '', '', '']
  const subscript = parts[2]
    .split('')
    .map((digit) => subscriptDigits[digit])
    .join('')
  return `${superscript}${parts[1]}${subscript}`
}
